package javaPrograms;

public class FindAsciiValue {

	public static void main(String[] args) {

     char ch;
   
     int ascii;
		
		
   
     
     for( ascii=65;ascii<=122;ascii++)
  
     {
    	  ch = ascii;
    	  
         System.out.println("Ascii values From A to z:- "+" " +ch +" "+ascii);
     }
         
	
     

	}

}
